import pandas as pd
from sklearn.linear_model import LinearRegression

# 샘플 데이터
data = {
    '작업자수': [10, 12, 8, 15, 11, 14, 9, 13, 16, 10, 12, 15, 8, 14, 11],
    '작업시간': [8, 10, 6, 9, 8, 10, 7, 9, 11, 8, 9, 10, 6, 9, 8],
    '원자재량': [100, 120, 80, 150, 110, 140, 90, 130, 160, 100, 120, 150, 80, 140, 110],
    '생산량': [520, 680, 380, 820, 560, 780, 420, 700, 900, 500, 640, 800, 360, 760, 580]
}

df = pd.DataFrame(data)

# 모델 학습
X = df[['작업자수', '작업시간', '원자재량']]
y = df['생산량']

model = LinearRegression()
model.fit(X, y)

print("========================================")
print("       생산량 예측 프로그램")
print("========================================")
print()

while True:
    print("--- 정보를 입력하세요 ---")
    
    작업자수 = input("작업자 수 (명): ")
    작업시간 = input("작업 시간 (시간): ")
    원자재량 = input("원자재량: ")
    
    # 숫자로 변환
    작업자수 = int(작업자수)
    작업시간 = int(작업시간)
    원자재량 = int(원자재량)
    
    # 예측
    결과 = model.predict([[작업자수, 작업시간, 원자재량]])[0]
    
    print()
    print("========================================")
    print("예상 생산량: {}개".format(round(결과)))
    print("========================================")
    print()
    
    계속 = input("다시 예측하시겠습니까? (y/n): ")
    if 계속.lower() != 'y':
        break
    print()

print("프로그램을 종료합니다.")
